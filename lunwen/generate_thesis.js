// ===== Helper functions =====
function p(text, opts = {}) {
  const runs = [];
  if (typeof text === 'string') {
    runs.push(new TextRun({ text, bold: opts.bold, font: opts.font || 'Times New Roman', size: opts.size || 24 }));
  } else if (Array.isArray(text)) {
    text.forEach(r => runs.push(new TextRun(r)));
  }
  return new Paragraph({
    children: runs,
    alignment: opts.alignment,
    spacing: { line: opts.line || 360, before: opts.before || 0, after: opts.after || 0 },
    indent: opts.indent ? { firstLine: opts.indent } : undefined,
    heading: opts.heading,
    pageBreakBefore: opts.pageBreakBefore,
  });
}

function h1(text) {
  return new Paragraph({
    heading: HeadingLevel.HEADING_1,
    alignment: AlignmentType.CENTER,
    spacing: { before: 240, after: 240, line: 360 },
    children: [new TextRun({ text, bold: true, font: 'SimHei', size: 32 })],
  });
}

